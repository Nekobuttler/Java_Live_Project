      public void review_Data(){
        String name, password;
       
        try{
            connect();
            name=Name.getText();
            password=Password.getText();
            Statement sentency = connect.createStatement();
            try{
            ResultSet rs=sentency.executeQuery("select * from Register where nombre= "+"'"+ name +"'" +"AND password ="+"'"+ password+"'");
            while(rs.next()){
               if(rs.getBoolean(7)==true){
                    JOptionPane.showMessageDialog(null, "Usted se encuentra baneado de la plataforma");
                    break;
               }else{
                  String u =Name.getText();
                  String p=Password.getText();
                   this.setVisible(false);
                   Stream_user a=new Stream_user();
                   a.getUpdateU(u);
                   a.getUpdateP(p);
                   a.setVisible(true);
                   dispose();
                   break;
               }
            }
            }catch(SQLException e){
                 JOptionPane.showMessageDialog(null, "An error had occur !! \n"
                    + "error: "+e);
            }
           
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null, "An error had occur !! \n"
                    + "error: "+ex);
            
        };
        disconect();
        
    }
    
    
    
    
    
    
    
